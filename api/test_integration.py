def _create_user(session_factory, password=TEST_PASSWORD):
    """Insert a user directly into the test DB. Returns user_id."""
    db = session_factory()
    try:
        user = User(password_hash=hash_password(password))
        db.add(user)
        db.commit()
        db.refresh(user)
        return user.id
    finally:
        db.close()


@pytest.fixture
def client(tmp_path, monkeypatch):
    """Litestar test client backed by a real SQLite file in tmp_path."""
    db_path = tmp_path / "shelf.db"
    test_engine = create_engine(
        f"sqlite:///{db_path}",
        echo=False,
        connect_args={"check_same_thread": False},
    )

    @event.listens_for(test_engine, "connect")
    def _set_pragma(dbapi_conn, connection_record):
        cursor = dbapi_conn.cursor()
        cursor.execute("PRAGMA foreign_keys=ON")
        cursor.close()

    test_session_local = sessionmaker(bind=test_engine, expire_on_commit=False)

    # Patch SessionLocal in every module that does `from db import SessionLocal`.
    # If a new file imports SessionLocal, it must be added here or it will
    # silently hit the real database.
    monkeypatch.setattr("db.engine", test_engine)
    monkeypatch.setattr("db.SessionLocal", test_session_local)
    monkeypatch.setattr("routes.auth.SessionLocal", test_session_local)
    monkeypatch.setattr("routes.transfers.SessionLocal", test_session_local)
    monkeypatch.setattr("main.SessionLocal", test_session_local)

    # Same for file storage paths — patch everywhere they're imported.
    transfers_dir = tmp_path / "transfers"
    thumbs_dir = tmp_path / "thumbs"
    transfers_dir.mkdir()
    thumbs_dir.mkdir()
    monkeypatch.setattr("config.TRANSFERS_DIR", transfers_dir)
    monkeypatch.setattr("config.THUMBS_DIR", thumbs_dir)
    monkeypatch.setattr("routes.transfers.TRANSFERS_DIR", transfers_dir)
    monkeypatch.setattr("routes.transfers.THUMBS_DIR", thumbs_dir)
    monkeypatch.setattr("main.TRANSFERS_DIR", transfers_dir)

    Base.metadata.create_all(test_engine)

    from main import app
    with TestClient(app=app, base_url="https://testserver.local") as tc:
        tc._session_factory = test_session_local
        tc._transfers_dir = transfers_dir
        tc._thumbs_dir = thumbs_dir
        yield tc


def _login(client, password=TEST_PASSWORD):
    _create_user(client._session_factory, password)
    resp = client.post("/auth/login", json={"password": password})
    assert resp.status_code == 200


def test_file_lifecycle(client):
    """Upload a file, list it, download it, delete it, verify disk cleanup."""
    _login(client)
    content = b"lifecycle test data"

    # Upload
    resp = client.post(
        "/transfers/upload",
        files={"data": ("lifecycle.bin", content, "application/octet-stream")},
    )
    assert resp.status_code == 201
    transfer_id = resp.json()["id"]

    # Listed
    resp = client.get("/transfers/")
    assert any(t["id"] == transfer_id for t in resp.json())

    # Download matches
    resp = client.get(f"/transfers/{transfer_id}/download")
    assert resp.status_code == 200
    assert resp.content == content
    assert resp.headers["cache-control"] == "no-cache"

    # File exists on disk
    user_dir = list(client._transfers_dir.iterdir())[0]
    file_path = user_dir / str(transfer_id)
    assert file_path.exists()

    # Delete
    resp = client.delete(f"/transfers/{transfer_id}")
    assert resp.status_code == 204

    # Gone from DB
    resp = client.get("/transfers/")
    assert len(resp.json()) == 0

    # Gone from disk
    assert not file_path.exists()


def test_text_lifecycle(client):
    """Create a text transfer, rename it, verify, delete it."""
    _login(client)

    resp = client.post("/transfers/", json={"type": "text", "content": "draft"})
    assert resp.status_code == 201
    transfer_id = resp.json()["id"]

    resp = client.patch(f"/transfers/{transfer_id}", json={"content": "final"})
    assert resp.status_code == 200
    assert resp.json()["content"] == "final"

    # Verify rename persisted
    resp = client.get("/transfers/")
    assert resp.json()[0]["content"] == "final"

    resp = client.delete(f"/transfers/{transfer_id}")
    assert resp.status_code == 204

    resp = client.get("/transfers/")
    assert len(resp.json()) == 0


def test_multi_user_file_isolation(client):
    """User B cannot list, download, or delete User A's file."""
    _create_user(client._session_factory, TEST_PASSWORD)
    _create_user(client._session_factory, TEST_PASSWORD_B)

    # User A uploads
    client.post("/auth/login", json={"password": TEST_PASSWORD})
    resp = client.post(
        "/transfers/upload",
        files={"data": ("secret.bin", b"A only", "application/octet-stream")},
    )
    file_id = resp.json()["id"]

    # Switch to User B
    client.post("/auth/logout")
    client.post("/auth/login", json={"password": TEST_PASSWORD_B})

    resp = client.get("/transfers/")
    assert len(resp.json()) == 0
    assert client.get(f"/transfers/{file_id}/download").status_code == 404
    assert client.delete(f"/transfers/{file_id}").status_code == 404

    # File still on disk (User A still owns it)
    client.post("/auth/logout")
    client.post("/auth/login", json={"password": TEST_PASSWORD})
    resp = client.get(f"/transfers/{file_id}/download")
    assert resp.status_code == 200
    assert resp.content == b"A only"


def test_storage_usage_tracks_deletes(client):
    """Upload 3 files, verify total, delete one, verify total decreases."""
    _login(client)
    sizes = [500, 300, 200]
    ids = []

    for i, size in enumerate(sizes):
        resp = client.post(
            "/transfers/upload",
            files={"data": (f"f{i}.bin", b"x" * size, "application/octet-stream")},
        )
        ids.append(resp.json()["id"])

    resp = client.get("/transfers/usage")
    assert resp.json()["used"] == 1000

    # Delete the 500-byte file
    client.delete(f"/transfers/{ids[0]}")

    resp = client.get("/transfers/usage")
    assert resp.json()["used"] == 500


def test_session_persistence_and_invalidation(client):
    """Login cookie works across requests and is invalidated by logout."""
    _login(client)

    # Cookie persists across multiple requests
    assert client.get("/auth/check").status_code == 200
    assert client.get("/transfers/").status_code == 200
    assert client.get("/auth/check").status_code == 200

    # Logout invalidates it
    client.post("/auth/logout")
    assert client.get("/auth/check").status_code == 401


def test_thumbnail_on_disk(client):
    """Upload an image, verify thumbnail is generated on disk as webp."""
    _login(client)

    resp = client.post(
        "/transfers/upload",
        files={"data": ("photo.png", TINY_PNG, "image/png")},
    )
    transfer_id = resp.json()["id"]

    resp = client.get(f"/transfers/{transfer_id}/thumbnail")
    assert resp.status_code == 200
    assert resp.headers["content-type"] == "image/webp"
    assert resp.headers["cache-control"] == "no-cache"

    # Thumbnail exists on disk
    user_dir = list(client._thumbs_dir.iterdir())[0]
    thumb_path = user_dir / f"{transfer_id}.webp"
    assert thumb_path.exists()

    # Deleting the transfer cleans up the thumbnail too
    client.delete(f"/transfers/{transfer_id}")
    assert not thumb_path.exists()
